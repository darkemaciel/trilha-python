# Crie um programa que solicite ao usuário dois números inteiros e exiba a soma desses números.

print("Informe dois valores para somar.")
input1 = int(input("Digite o primeiro valor: "))
input2 = int(input("Digite o segundo valor: "))
result = input1 + input2
print(f"A soma de {input1} e {input2} é {result}.")